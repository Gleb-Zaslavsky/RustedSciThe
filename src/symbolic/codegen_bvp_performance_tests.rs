fn eval_residual_values_from_plan_and_chunks(
    args: &[f64],
    plan: &crate::symbolic::codegen_runtime_api::ResidualRuntimePlan<'_>,
    chunk_fns: &[GeneratedChunkFn],
) -> Vec<f64> {
    assert_eq!(plan.chunks.len(), chunk_fns.len());
    let mut out = vec![0.0; plan.output_len];
    for (chunk, chunk_fn) in plan.chunks.iter().zip(chunk_fns.iter().copied()) {
        let start = chunk.output_offset;
        let end = start + chunk.residuals.len();
        chunk_fn(args, &mut out[start..end]);
    }
    out
}

fn eval_sparse_values_from_plan_and_chunks(
    args: &[f64],
    plan: &crate::symbolic::codegen_runtime_api::SparseJacobianRuntimePlan<'_>,
    chunk_fns: &[GeneratedChunkFn],
) -> Vec<f64> {
    assert_eq!(plan.chunks.len(), chunk_fns.len());
    let mut out = vec![0.0; plan.nnz()];
    for (chunk, chunk_fn) in plan.chunks.iter().zip(chunk_fns.iter().copied()) {
        let start = chunk.value_offset;
        let end = start + chunk.entries.len();
        chunk_fn(args, &mut out[start..end]);
    }
    out
}

fn bind_residual_fixture_chunks<'a>(
    plan: &'a crate::symbolic::codegen_runtime_api::ResidualRuntimePlan<'a>,
    chunk_fns: &[GeneratedChunkFn],
) -> Vec<ResidualChunkBinding<'a>> {
    assert_eq!(plan.chunks.len(), chunk_fns.len());
    plan.chunks
        .iter()
        .zip(chunk_fns.iter().copied())
        .map(|(chunk, eval)| ResidualChunkBinding {
            fn_name: chunk.fn_name.as_str(),
            output_offset: chunk.output_offset,
            output_len: chunk.residuals.len(),
            eval,
        })
        .collect()
}

fn bind_sparse_fixture_chunks<'a>(
    plan: &'a crate::symbolic::codegen_runtime_api::SparseJacobianRuntimePlan<'a>,
    chunk_fns: &[GeneratedChunkFn],
) -> Vec<SparseJacobianChunkBinding<'a>> {
    assert_eq!(plan.chunks.len(), chunk_fns.len());
    plan.chunks
        .iter()
        .zip(chunk_fns.iter().copied())
        .map(|(chunk, eval)| SparseJacobianChunkBinding {
            fn_name: chunk.fn_name.as_str(),
            value_offset: chunk.value_offset,
            value_len: chunk.entries.len(),
            eval,
        })
        .collect()
}

