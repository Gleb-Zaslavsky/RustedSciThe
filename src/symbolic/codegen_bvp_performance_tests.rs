#![cfg(test)]

//! Performance and equivalence diagnostics for symbolic BVP code generation.
//!
//! This module compares several execution paths:
//! - the legacy `parallel2` lambdified pipeline,
//! - sequential AOT execution,
//! - parallel AOT execution,
//! - and, for some sweeps, IR-level AOT execution without checked-in Rust fixtures.
//!
//! Baselines and runtime paths used across the file:
//! - legacy BVP residual/Jacobian generation and execution,
//! - IR-level AOT lowering/evaluation,
//! - checked-in compiled fixture modules,
//! - sequential and parallel orchestrator execution over the same chunk plans.
//!
//! The file is intentionally test-heavy. It acts as a lab notebook for several
//! hypotheses about when AOT becomes worthwhile and when intra-call parallelism
//! starts to pay off.
//!
//! # What The Main Test Groups Cover
//!
//! ## 1. Real Small BVP Checks
//! These use `build_real_bvp_damp1_case(...)`.
//! - Problem shape: `2` equations on a modest grid.
//! - Typical unknown count: tens of variables.
//! - Purpose: semantic sanity checks, early perf smoke tests, and regression
//!   tests for the basic codegen/orchestrator path.
//! - Relevant tests include the `real_bvp_*` and small compiled fixture tests.
//!
//! ## 2. Large Single-Problem Chunking Experiments
//! These still use the `2`-equation BVP family, but with larger `n_steps` and
//! multiple emitted fixture variants.
//! - Problem shape: still `2` equations, but roughly hundreds of unknowns.
//! - Purpose: test chunk-size hypotheses such as `O4/O8/O16`, `R4/R8/R16`,
//!   super-block chunking, and orchestration overhead.
//! - Relevant tests include:
//!   - `benchmark_large_real_bvp_*`
//!   - `benchmark_large_real_bvp_compiled_chunk_variants_runtime`
//!   - `benchmark_large_real_bvp_superblock_variants_runtime`
//!
//! ## 3. Stress Multifield BVP Family
//! These use `build_multifield_stress_bvp_case(field_count, n_steps)`.
//! - Problem shape: `field_count` coupled equations over `n_steps` grid points.
//! - Unknown count: approximately `field_count * n_steps`.
//! - Jacobian shape: approximately `(field_count * n_steps) x (field_count * n_steps)`.
//! - Purpose: probe realistic scaling toward production cases with many coupled
//!   right-hand sides.
//! - These are the most important tests for finding where parallel AOT begins
//!   to outperform sequential AOT.
//!
//! ## 4. Compiled Stress Fixtures
//! These are real checked-in Rust modules generated by the AOT pipeline.
//! They are the strongest "production-like" evidence in this file because the
//! measured code is emitted Rust, compiled by Rust, and then executed through
//! the orchestrator layer.
//!
//! Current compiled stress fixtures:
//! - `test_codegen_generated_stress_bvp_fixtures`
//!   - baseline stress case
//!   - currently `field_count = 8`, `n_steps = 96`
//!   - roughly `768` unknowns
//! - `test_codegen_generated_stress_large_bvp_fixtures`
//!   - near-threshold compiled case
//!   - currently `field_count = 16`, `n_steps = 96`
//!   - roughly `1536` unknowns
//! - `test_codegen_generated_stress_above_threshold_bvp_fixtures`
//!   - above-threshold compiled case
//!   - currently `field_count = 32`, `n_steps = 96`
//!   - roughly `3072` unknowns
//!
//! In current measurements:
//! - the `16 x 96` compiled case behaves like a near-threshold case,
//! - the `32 x 96` compiled case behaves like an above-threshold case,
//! - and the IR-level crossover search suggests the practical boundary is
//!   around `field_count = 8` for residuals and around `field_count = 16` for
//!   sparse Jacobian values when `n_steps` is around `96..128`.
//!
//! # Important Named Benchmarks
//!
//! - `benchmark_stress_multifield_bvp_pipeline`
//!   Measures symbolic build, lambdify compilation, AOT planning, AOT assembly,
//!   and emitted-source cost on a stress family.
//!
//! - `benchmark_stress_multifield_scaling_sweep`
//!   IR-level scaling benchmark. This is not a compiled fixture benchmark.
//!   It lowers runtime plans into `LinearBlock`s and compares sequential AOT IR
//!   vs parallel AOT IR over a size sweep.
//!
//! - `benchmark_stress_multifield_parallel_crossover`
//!   Searches for the first stress size where parallel AOT IR becomes faster
//!   than sequential AOT IR for:
//!   - residual evaluation
//!   - sparse Jacobian value evaluation
//!
//! - `benchmark_compiled_stress_multifield_aot_runtime`
//!   Compiled Rust benchmark for the baseline stress fixture.
//!
//! - `benchmark_compiled_large_stress_multifield_aot_runtime`
//!   Compiled Rust benchmark for the near-threshold `16 x 96` stress fixture.
//!
//! - `benchmark_compiled_above_threshold_stress_multifield_aot_runtime`
//!   Compiled Rust benchmark for the above-threshold `32 x 96` stress fixture.
//!
//! # Reading The Results
//!
//! These tests are diagnostics, not strict performance assertions:
//! - they print stage timings and hot-path timings,
//! - they retain correctness checks where practical,
//! - but they do not fail based on performance ratios.
//!
//! The main questions they help answer are:
//! - Does AOT match `parallel2` semantically?
//! - Does sequential AOT beat the legacy lambdified path?
//! - At what problem size does parallel AOT start beating sequential AOT?

