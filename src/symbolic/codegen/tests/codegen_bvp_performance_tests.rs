fn eval_residual_values_from_plan_and_chunks(
    args: &[f64],
    plan: &crate::symbolic::codegen::codegen_runtime_api::ResidualRuntimePlan<'_>,
    chunk_fns: &[GeneratedChunkFn],
) -> Vec<f64> {
    assert_eq!(plan.chunks.len(), chunk_fns.len());
    let mut out = vec![0.0; plan.output_len];
    for (chunk, chunk_fn) in plan.chunks.iter().zip(chunk_fns.iter().copied()) {
        let start = chunk.output_offset;
        let end = start + chunk.residuals.len();
        chunk_fn(args, &mut out[start..end]);
    }
    out
}

