fn block_column_nonzero_counts(block: &[f64], bs: usize) -> Vec<usize> {
    (0..bs)
        .map(|col| {
            (0..bs)
                .filter(|&row| block[row * bs + col].abs() > 1.0e-14)
                .count()
        })
        .collect()
}

fn block_column_max_abs(block: &[f64], bs: usize) -> Vec<f64> {
    (0..bs)
        .map(|col| {
            (0..bs)
                .map(|row| block[row * bs + col].abs())
                .fold(0.0_f64, f64::max)
        })
        .collect()
}

fn format_block(block: &[f64], bs: usize) -> String {
    let mut out = String::new();
    for row in 0..bs {
        let line = (0..bs)
            .map(|col| format!("{:>12.4e}", block[row * bs + col]))
            .collect::<Vec<_>>()
            .join(" ");
        out.push_str(&line);
        out.push('\n');
    }
    out
}

fn format_block_column_stats(block: &[f64], bs: usize) -> String {
    let nnz = block_column_nonzero_counts(block, bs);
    let max_abs = block_column_max_abs(block, bs);
    nnz.iter()
        .zip(max_abs.iter())
        .map(|(nnz, max_abs)| format!("{nnz}:{max_abs:.2e}"))
        .collect::<Vec<_>>()
        .join(", ")
}

fn dense_linf_diff(lhs: &[Vec<f64>], rhs: &[Vec<f64>]) -> f64 {
    lhs.iter()
        .zip(rhs.iter())
        .flat_map(|(lhs_row, rhs_row)| lhs_row.iter().zip(rhs_row.iter()))
        .map(|(lhs, rhs)| (lhs - rhs).abs())
        .fold(0.0_f64, f64::max)
}

fn dense_matmul_square(lhs: &[Vec<f64>], rhs: &[Vec<f64>]) -> Vec<Vec<f64>> {
    let n = lhs.len();
    let mut out = vec![vec![0.0; n]; n];
    for i in 0..n {
        for k in 0..n {
            let lhs_ik = lhs[i][k];
            if lhs_ik == 0.0 {
                continue;
            }
            for j in 0..n {
                out[i][j] += lhs_ik * rhs[k][j];
            }
        }
    }
    out
}

fn permutation_from_swap_pivots(pivots: &[usize]) -> Vec<usize> {
    let mut perm: Vec<usize> = (0..pivots.len()).collect();
    for (k, &p) in pivots.iter().enumerate() {
        perm.swap(k, p);
    }
    perm
}

fn apply_block_row_permutations(
    dense: &[Vec<f64>],
    block_size: usize,
    pivots_by_block: &[Vec<usize>],
) -> Vec<Vec<f64>> {
    let n = dense.len();
    let mut out = dense.to_vec();
    for (block_idx, pivots) in pivots_by_block.iter().enumerate() {
        let perm = permutation_from_swap_pivots(pivots);
        let row0 = block_idx * block_size;
        let mut permuted_rows = vec![vec![0.0; n]; block_size];
        for local_row in 0..block_size {
            permuted_rows[local_row] = dense[row0 + perm[local_row]].clone();
        }
        for local_row in 0..block_size {
            out[row0 + local_row] = permuted_rows[local_row].clone();
        }
    }
    out
}

fn split_dense_lu_block(lu: &[f64], bs: usize) -> (Vec<f64>, Vec<f64>) {
    let mut l = vec![0.0; bs * bs];
    let mut u = vec![0.0; bs * bs];
    for i in 0..bs {
        for j in 0..bs {
            let idx = i * bs + j;
            if i > j {
                l[idx] = lu[idx];
            } else if i == j {
                l[idx] = 1.0;
                u[idx] = lu[idx];
            } else {
                u[idx] = lu[idx];
            }
        }
    }
    (l, u)
}

fn reconstruct_block_lu_factors(lu: &BlockTridiagonalLu) -> (Vec<Vec<f64>>, Vec<Vec<f64>>) {
    let nb = lu.n_blocks();
    let bs = lu.block_size();
    let n = lu.n();
    let mut l = vec![vec![0.0; n]; n];
    let mut u = vec![vec![0.0; n]; n];

    for block_idx in 0..nb {
        let row0 = block_idx * bs;
        let col0 = block_idx * bs;
        let (l_diag, u_diag) = split_dense_lu_block(&lu.diag_lu_blocks()[block_idx], bs);

        for i in 0..bs {
            for j in 0..bs {
                l[row0 + i][col0 + j] = l_diag[i * bs + j];
                u[row0 + i][col0 + j] = u_diag[i * bs + j];
            }
        }

        if block_idx + 1 < nb {
            let lower = &lu.lower_mult_blocks()[block_idx];
            let upper = &lu.upper_blocks()[block_idx];
            let lower_row0 = (block_idx + 1) * bs;
            let lower_col0 = block_idx * bs;
            let upper_row0 = block_idx * bs;
            let upper_col0 = (block_idx + 1) * bs;

            for i in 0..bs {
                for j in 0..bs {
                    l[lower_row0 + i][lower_col0 + j] = lower[i * bs + j];
                    u[upper_row0 + i][upper_col0 + j] = upper[i * bs + j];
                }
            }
        }
    }

    (l, u)
}

/// Focused structural diagnostic for the combustion banded path.
