/// Superblock experiment for the combustion banded path.
///
/// The goal is not to benchmark speed, but to answer one structural question:
/// does grouping several neighboring nodes into one solver block remove the
/// local singularity that makes the plain `6x6` node-major block fail?
pub(crate) fn run_diagnose_combustion_superblock_groupings() {
    #[derive(Debug)]
    struct SuperblockRow {
        scenario: String,
        group: String,
        solver: String,
        layout: String,
        first_diag_cols: String,
        status: String,
        error: String,
        factor_residual: String,
        solve_diff: String,
        block_residual: String,
    }

    let mut rows = Vec::new();

    for n in [100usize, 1000usize] {
        let jac = build_combustion_bvp_case(n);
        let prepared = jac.prepare_sparse_aot_problem(
            "diag_combustion_superblock_residual",
            "diag_combustion_superblock_sparse_values",
            ResidualChunkingStrategy::Whole,
            SparseChunkingStrategy::Whole,
        );
        let input_names = prepared.as_prepared_problem().input_names().to_vec();
        let args: Vec<f64> = (0..input_names.len())
            .map(|index| 0.25 + index as f64 * 1.0e-4)
            .collect();

        let sparse_entries_owned = jac.symbolic_jacobian_sparse_entries_owned();
        let sparse_fns =
            compile_sparse_lambdify(input_names.as_slice(), sparse_entries_owned.as_slice());
        let sparse_values = eval_functions(&sparse_fns, &args);
        let sparse_prepared = prepared.as_prepared_problem();
        let sparse_matrix = sparse_prepared
            .jacobian_plan
            .assemble_sparse_col_mat(sparse_values.as_slice());
        let rhs = sample_rhs(sparse_matrix.nrows());
        let sparse_solution = solve_sparse_lu(&sparse_matrix, rhs.as_slice());

        let banded_prepared = prepared.as_prepared_banded_problem();
        let banded_fns = compile_banded_lambdify(&prepared);
        let banded_values = eval_functions(&banded_fns, &args);
        let assembly = banded_prepared
            .jacobian_plan
            .assemble_banded_assembly(banded_values.as_slice());

        let node_layout = infer_node_major_layout(&jac, assembly.n());
        let n_nodes = node_layout.n_nodes();
        let vars_per_node = node_layout.vars_per_node();

        for nodes_per_superblock in [1usize, 2, 3, 4] {
            if n_nodes % nodes_per_superblock != 0 {
                rows.push(SuperblockRow {
                    scenario: format!("combustion-{n}"),
                    group: nodes_per_superblock.to_string(),
                    solver: "n/a".to_string(),
                    layout: format!("skip/{vars_per_node}"),
                    first_diag_cols: "-".to_string(),
                    status: "skipped".to_string(),
                    error: "n_nodes is not divisible by group".to_string(),
                    factor_residual: "-".to_string(),
                    solve_diff: "-".to_string(),
                    block_residual: "-".to_string(),
                });
                continue;
            }

            let layout = SuperBlockLayout::new(n_nodes, vars_per_node, nodes_per_superblock)
                .expect("superblock layout should be valid");
            let block = assembly
                .finalize_superblock_tridiagonal(&layout)
                .expect("superblock layout should convert to uniform block-tridiagonal storage");
            let first_diag = block.diag_block(0).expect("first diagonal block exists");

            let mut legacy_lu = BlockTridiagonalLu::new(block.n_blocks(), block.block_size())
                .expect("native block LU workspace should be constructible");
            let mut consistent_lu =
                BlockTridiagonalLuConsistent::new(block.n_blocks(), block.block_size())
                    .expect("consistent block LU workspace should be constructible");

            rows.push(SuperblockRow {
                scenario: format!("combustion-{n}"),
                group: nodes_per_superblock.to_string(),
                solver: "legacy".to_string(),
                layout: format!("{}x{}", layout.n_blocks(), layout.block_size()),
                first_diag_cols: format_block_column_stats(first_diag, layout.block_size()),
                status: match legacy_lu.factor_from(&block) {
                    Ok(()) => "ok".to_string(),
                    Err(_) => "failed".to_string(),
                },
                error: "-".to_string(),
                factor_residual: "-".to_string(),
                solve_diff: "-".to_string(),
                block_residual: "-".to_string(),
            });

            if let Err(err) = legacy_lu.factor_from(&block) {
                if let Some(last) = rows.last_mut() {
                    last.error = format!("{err:?}");
                }
            } else if let Some(last) = rows.last_mut() {
                let dense_a = block.to_dense();
                let dense_pa = apply_block_row_permutations(
                    dense_a.as_slice(),
                    block.block_size(),
                    legacy_lu.diag_pivots(),
                );
                let (dense_l, dense_u) = reconstruct_block_lu_factors(&legacy_lu);
                let dense_lu = dense_matmul_square(dense_l.as_slice(), dense_u.as_slice());
                let mut rhs_native = rhs.clone();
                legacy_lu.solve_in_place(rhs_native.as_mut_slice()).expect(
                    "legacy superblock solve should succeed after successful factorization",
                );
                let block_ax = crate::somelinalg::banded::ops::dense_matvec(
                    dense_a.as_slice(),
                    rhs_native.as_slice(),
                );
                last.factor_residual = format!(
                    "{:.3e}",
                    dense_linf_diff(dense_pa.as_slice(), dense_lu.as_slice())
                );
                last.solve_diff = format!("{:.3e}", max_abs_diff(&rhs_native, &sparse_solution));
                last.block_residual = format!(
                    "{:.3e}",
                    crate::somelinalg::banded::ops::vec_diff_linf(
                        block_ax.as_slice(),
                        rhs.as_slice(),
                    )
                );
            }

            rows.push(SuperblockRow {
                scenario: format!("combustion-{n}"),
                group: nodes_per_superblock.to_string(),
                solver: "consistent".to_string(),
                layout: format!("{}x{}", layout.n_blocks(), layout.block_size()),
                first_diag_cols: format_block_column_stats(first_diag, layout.block_size()),
                status: match consistent_lu.factor_from(&block) {
                    Ok(()) => "ok".to_string(),
                    Err(_) => "failed".to_string(),
                },
                error: "-".to_string(),
                factor_residual: "-".to_string(),
                solve_diff: "-".to_string(),
                block_residual: "-".to_string(),
            });
            if let Err(err) = consistent_lu.factor_from(&block) {
                if let Some(last) = rows.last_mut() {
                    last.error = format!("{err:?}");
                }
            } else if let Some(last) = rows.last_mut() {
                let dense_a = block.to_dense();
                let mut rhs_native = rhs.clone();
                consistent_lu
                    .solve_in_place(rhs_native.as_mut_slice())
                    .expect(
                        "consistent superblock solve should succeed after successful factorization",
                    );
                let block_ax = crate::somelinalg::banded::ops::dense_matvec(
                    dense_a.as_slice(),
                    rhs_native.as_slice(),
                );
                last.factor_residual = format!(
                    "{:.3e}",
                    consistent_lu
                        .factor_residual_inf(&block)
                        .expect("consistent factor residual should be available")
                );
                last.solve_diff = format!("{:.3e}", max_abs_diff(&rhs_native, &sparse_solution));
                last.block_residual = format!(
                    "{:.3e}",
                    crate::somelinalg::banded::ops::vec_diff_linf(
                        block_ax.as_slice(),
                        rhs.as_slice(),
                    )
                );
            }
        }
    }

    let mut builder = Builder::default();
    builder.push_record([
        "scenario",
        "group",
        "solver",
        "layout",
        "first_diag_cols",
        "status",
        "error",
        "factor_residual",
        "solve_diff",
        "block_residual",
    ]);
    for row in rows {
        builder.push_record([
            row.scenario,
            row.group,
            row.solver,
            row.layout,
            row.first_diag_cols,
            row.status,
            row.error,
            row.factor_residual,
            row.solve_diff,
            row.block_residual,
        ]);
    }
    let mut table = builder.build();
    table.with(Style::rounded());
    println!("[BVP superblock diagnostic] combustion superblock grouping table");
    println!("{table}");
}

