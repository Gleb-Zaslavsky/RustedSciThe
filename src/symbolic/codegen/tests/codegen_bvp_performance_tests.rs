#[test]
fn real_bvp_parallel2_and_generated_aot_match_exactly_on_same_inputs() {
    let mut jac = build_real_bvp_damp1_case(8);
    let variable_names_owned = jac.variable_string.clone();
    let variable_names: Vec<&str> = variable_names_owned
        .iter()
        .map(|name| name.as_str())
        .collect();
    jac.lambdify_residual_Col_parallel2("x", variable_names.clone());
    jac.lambdify_jacobian_SparseColMat_parallel2("x", variable_names.clone());

    let variable_values: Vec<f64> = (0..jac.variable_string.len())
        .map(|index| 0.2 + index as f64 * 0.01)
        .collect();
    let runtime_args = RuntimeArguments::new(variable_values.as_slice(), None);
    let flat_args = runtime_args.flatten();
    let input_col = ColRef::from_slice(flat_args.as_slice()).to_owned();

    let residual_parallel2 = jac
        .residiual_function
        .call(0.0, &input_col)
        .to_DVectorType();
    let residual_generated = eval_generated_bvp_residual_col(flat_args.as_slice());
    let residual_generated_dense: Vec<f64> =
        residual_generated.iter().map(|value| *value).collect();

    assert_eq!(residual_parallel2.len(), residual_generated_dense.len());
    for (lhs, rhs) in residual_parallel2
        .iter()
        .zip(residual_generated_dense.iter())
    {
        let lhs = *lhs;
        let rhs = *rhs;
        assert!(
            (lhs - rhs).abs() < 1e-11,
            "residual mismatch: {lhs} vs {rhs}"
        );
    }

    let sparse_entries_owned = jac.symbolic_jacobian_sparse_entries_owned();
    let sparse_entries = borrowed_sparse_entries(&sparse_entries_owned);
    let sparse_task = SparseJacobianTask {
        fn_name: "fixture_bvp_sparse_values",
        shape: (jac.vector_of_functions.len(), jac.vector_of_variables.len()),
        entries: &sparse_entries,
        variables: &variable_names,
        params: None,
    };
    let sparse_runtime_plan =
        sparse_task.runtime_plan(SparseChunkingStrategy::ByRowCount { rows_per_chunk: 1 });

    let jacobian_parallel2 = jac
        .jac_function
        .as_mut()
        .expect("parallel2 Jacobian should be compiled")
        .call(0.0, &input_col)
        .to_DMatrixType();
    let jacobian_generated_values = eval_generated_bvp_sparse_values(flat_args.as_slice());
    let jacobian_generated = sparse_runtime_plan
        .assemble_sparse_col_mat(jacobian_generated_values.as_slice())
        .to_DMatrixType();

    assert_eq!(jacobian_parallel2.shape(), jacobian_generated.shape());
    for i in 0..jacobian_parallel2.nrows() {
        for j in 0..jacobian_parallel2.ncols() {
            let lhs = jacobian_parallel2[(i, j)];
            let rhs = jacobian_generated[(i, j)];
            assert!(
                (lhs - rhs).abs() < 1e-11,
                "jacobian mismatch at ({i}, {j}): {lhs} vs {rhs}"
            );
        }
    }
}

