//     Sweeps requested_jobs 1..=2*threads on the compiled stress fixture.
//     Prints actual_jobs, uses_sequential_fallback(), nnz/job, and ns/call.
//     Exposes whether the Auto fallback is silently killing the parallel path
//     and whether each job has enough work to amortize scheduling cost.
//
//  3. PROBLEM-SIZE CROSSOVER (IR-level, no compiled fixture needed)
//     Sweeps field_count at fixed n_steps using LinearBlock IR evaluation.
//     Prints the first field_count where parallel IR beats sequential IR.
//     Answers: "is the problem simply too small?"
