/// Measures raw rayon::join overhead with no-op closures.
/// Run with: cargo test diagnose_rayon_overhead -- --nocapture --ignored
/// Measures raw rayon::join cost with no-op closures (2-way and 4-way nested).
/// If the sparse eval time per call is within ~5× of this overhead, parallelism is
/// structurally impossible to help at that problem size — the scheduler costs more than the math.
pub(crate) fn run_diagnose_rayon_overhead_baseline() {
    const ITERS: usize = 10_000;
    const SAMPLES: usize = 9;
    let workers = rayon::current_num_threads();

    let seq_timings: Vec<Duration> = (0..SAMPLES)
        .map(|_| {
            let t = Instant::now();
            for _ in 0..ITERS {
                black_box(0u64);
            }
            t.elapsed()
        })
        .collect();

    let join2_timings: Vec<Duration> = (0..SAMPLES)
        .map(|_| {
            let t = Instant::now();
            for _ in 0..ITERS {
                rayon::join(|| black_box(0u64), || black_box(0u64));
            }
            t.elapsed()
        })
        .collect();

    let join4_timings: Vec<Duration> = (0..SAMPLES)
        .map(|_| {
            let t = Instant::now();
            for _ in 0..ITERS {
                rayon::join(
                    || rayon::join(|| black_box(0u64), || black_box(0u64)),
                    || rayon::join(|| black_box(0u64), || black_box(0u64)),
                );
            }
            t.elapsed()
        })
        .collect();

    let seq_ns = per_iter_ns(median_duration(seq_timings), ITERS);
    let join2_ns = per_iter_ns(median_duration(join2_timings), ITERS);
    let join4_ns = per_iter_ns(median_duration(join4_timings), ITERS);

    println!(
        "\n=== Rayon overhead baseline (workers={workers}, iters={ITERS}) ===\
         \nno-op sequential:          {seq_ns:.1} ns/call\
         \nno-op join (2-way):        {join2_ns:.1} ns/call  (overhead +{:.1} ns)\
         \nno-op join (4-way nested): {join4_ns:.1} ns/call  (overhead +{:.1} ns)\
         \n\nIf sparse eval time is within ~5x of join overhead, parallelism\
         \ncannot help at this problem size regardless of chunk count.",
        join2_ns - seq_ns,
        join4_ns - seq_ns,
    );
}

/// Sweeps requested_jobs on the compiled stress fixture and prints Auto-policy
/// diagnostics plus forced-parallel timings for comparison.
/// Run with: cargo test diagnose_chunk_granularity -- --nocapture --ignored
/// diagnose_chunk_granularity_and_fallback
///Uses the compiled stress fixture (8 fields × 96 steps) and sweeps requested_jobs from 1 to 2 × thread_count.
/// For each configuration it prints:
/// actual_jobs — how many jobs the weighted splitter actually produced (capped by chunk count)
///uses_sequential_fallback() — whether the Auto policy silently fell back even with Never override
/// nnz/job — work per job; if this is < ~50, rayon overhead dominates
/// ns/call — actual timing with <-- faster marker
pub(crate) fn run_diagnose_chunk_granularity_and_fallback() {
    let samples = stress_samples(9);
    let jacobian_iters = stress_jacobian_iters(200);
    let max_threads = rayon::current_num_threads();

    let field_count = generated_stress_bvp_fixture_bindings::FIELD_COUNT;
    let n_steps = generated_stress_bvp_fixture_bindings::N_STEPS;

    let jac = build_multifield_stress_bvp_case(field_count, n_steps);
    let variable_names_owned = jac.variable_string.clone();
    let variable_names: Vec<&str> = variable_names_owned.iter().map(|s| s.as_str()).collect();
    let flat_args = RuntimeArguments::new(
        &(0..jac.variable_string.len())
            .map(|i| 0.05 + i as f64 * 0.0005)
            .collect::<Vec<_>>(),
        None,
    )
    .flatten();

    let sparse_entries_owned = jac.symbolic_jacobian_sparse_entries_owned();
    let sparse_entries = borrowed_sparse_entries(&sparse_entries_owned);
    let sparse_task = SparseJacobianTask {
        fn_name: "diag_sparse",
        shape: (jac.vector_of_functions.len(), jac.vector_of_variables.len()),
        entries: &sparse_entries,
        variables: &variable_names,
        params: None,
    };
    // Same chunking as the compiled stress fixture.
    let sparse_plan =
        sparse_task.runtime_plan(SparseChunkingStrategy::ByRowCount { rows_per_chunk: 48 });
    let nnz = sparse_plan.nnz();
    let chunk_count = sparse_plan.chunks.len();
    let sparse_bindings = bind_sparse_fixture_chunks(&sparse_plan, stress_sparse_chunk_fns());
    let auto_probe = ParallelSparseJacobianExecutor::new(&sparse_plan, sparse_bindings.clone());
    let min_work = auto_probe.min_work_per_parallel_job();
    let nnz_per_chunk = auto_probe.work_per_chunk();

    println!(
        "\n=== Chunk granularity / fallback diagnostic ===\
         \nfields={field_count}, steps={n_steps}, nnz={nnz}, chunks={chunk_count}\
         \nrayon_threads={max_threads}, samples={samples}, jacobian_iters={jacobian_iters}\
         \nAuto policy derives min_work/job from measured rayon overhead.\
         \nmin_work/job={min_work}, nnz/chunk={nnz_per_chunk}\n"
    );
    println!(
        "{:<16} {:<12} {:<10} {:<10} {:<10} {:<12}",
        "mode", "jobs", "fallback", "nnz/job", "nnz/chunk", "ns/call"
    );

    // Sequential baseline.
    let seq = SequentialSparseJacobianExecutor::new(&sparse_plan, sparse_bindings.clone());
    let seq_ns = per_iter_ns(
        median_duration(
            (0..samples)
                .map(|_| {
                    let t = Instant::now();
                    for _ in 0..jacobian_iters {
                        black_box(seq.eval_values(&flat_args)[0]);
                    }
                    t.elapsed()
                })
                .collect(),
        ),
        jacobian_iters,
    );
    println!(
        "{:<16} {:<12} {:<10} {:<10} {:<10} {:.2}",
        "sequential", "-", "-", nnz, "-", seq_ns
    );

    let auto_ns = per_iter_ns(
        median_duration(
            (0..samples)
                .map(|_| {
                    let t = Instant::now();
                    for _ in 0..jacobian_iters {
                        black_box(auto_probe.eval_values(&flat_args)[0]);
                    }
                    t.elapsed()
                })
                .collect(),
        ),
        jacobian_iters,
    );
    println!(
        "{:<16} {:<12} {:<10} {:<10} {:<10} {:.2}  {}",
        "auto",
        auto_probe.job_count(),
        auto_probe.uses_sequential_fallback(),
        auto_probe.work_per_job(),
        auto_probe.work_per_chunk(),
        auto_ns,
        if auto_ns < seq_ns { "<-- faster" } else { "" }
    );

    println!("\nForced sweep (`ParallelFallbackPolicy::Never`) for diagnostics:");
    println!(
        "{:<16} {:<12} {:<10} {:<10} {:<10} {:<12}",
        "requested", "actual", "fallback", "nnz/job", "nnz/chunk", "ns/call"
    );

    // Parallel sweep with Never fallback so we always exercise the parallel path.
    for requested_jobs in 1..=(2 * max_threads).min(chunk_count * 2).max(4) {
        let config = ParallelExecutorConfig {
            jobs_per_worker: 1,
            max_residual_jobs: None,
            max_sparse_jobs: Some(requested_jobs),
            fallback_policy: ParallelFallbackPolicy::Never,
        };
        let par = ParallelSparseJacobianExecutor::with_config(
            &sparse_plan,
            sparse_bindings.clone(),
            config,
        );
        let ns = per_iter_ns(
            median_duration(
                (0..samples)
                    .map(|_| {
                        let t = Instant::now();
                        for _ in 0..jacobian_iters {
                            black_box(par.eval_values(&flat_args)[0]);
                        }
                        t.elapsed()
                    })
                    .collect(),
            ),
            jacobian_iters,
        );
        println!(
            "{:<16} {:<12} {:<10} {:<10} {:<10} {:.2}  {}",
            requested_jobs,
            par.job_count(),
            par.uses_sequential_fallback(),
            par.work_per_job(),
            par.work_per_chunk(),
            ns,
            if ns < seq_ns { "<-- faster" } else { "" }
        );
    }
}

#[derive(Clone, Debug)]
struct IrAmplificationRow {
    matrix: &'static str,
    stage: &'static str,
    strategy: String,
    chunks: usize,
    outputs: usize,
    instructions: usize,
    max_block_instructions: usize,
    temps: usize,
    source_kb: f64,
}

fn ir_amplification_row(
    matrix: &'static str,
    stage: &'static str,
    strategy: impl Into<String>,
    chunks: usize,
    blocks: Vec<(usize, LinearBlock)>,
    source_len: usize,
) -> IrAmplificationRow {
    let mut outputs = 0usize;
    let mut instructions = 0usize;
    let mut max_block_instructions = 0usize;
    let mut temps = 0usize;
    for (_, block) in blocks {
        outputs += block.outputs.len();
        instructions += block.instructions.len();
        max_block_instructions = max_block_instructions.max(block.instructions.len());
        temps += block.num_temps;
    }

    IrAmplificationRow {
        matrix,
        stage,
        strategy: strategy.into(),
        chunks,
        outputs,
        instructions,
        max_block_instructions,
        temps,
        source_kb: source_len as f64 / 1024.0,
    }
}

fn ir_amplification_baselines(
    rows: &[IrAmplificationRow],
) -> HashMap<(&'static str, &'static str), usize> {
    rows.iter()
        .filter(|row| row.strategy == "whole")
        .map(|row| ((row.matrix, row.stage), row.instructions.max(1)))
        .collect()
}

/// Prints whether generated chunking inflates IR/source work before runtime
/// scheduling and FFI even start.
pub(crate) fn run_diagnose_combustion_chunk_ir_amplification() {
    let n_steps = env_usize("BVP_CHUNK_IR_STEPS", 1000);
    let target_chunks = [
        ("whole", 1usize),
        ("chunk4", 4usize),
        ("chunk8", 8usize),
        ("chunk16", 16usize),
    ];

    let jac =
        build_combustion_bvp_case_with_backend(n_steps, BvpSymbolicAssemblyBackend::AtomView);
    let mut rows = Vec::new();

    for (label, chunks) in target_chunks {
        let residual_strategy = if chunks == 1 {
            ResidualChunkingStrategy::Whole
        } else {
            ResidualChunkingStrategy::ByTargetChunkCount {
                target_chunks: chunks,
            }
        };
        let sparse_strategy = if chunks == 1 {
            SparseChunkingStrategy::Whole
        } else {
            SparseChunkingStrategy::ByTargetChunkCount {
                target_chunks: chunks,
            }
        };

        let prepared = jac.prepare_sparse_aot_problem(
            "diagnose_combustion_residual",
            "diagnose_combustion_jacobian_values",
            residual_strategy,
            sparse_strategy,
        );
        let sparse_prepared = prepared.as_prepared_problem();
        let banded_prepared = prepared.as_prepared_banded_problem();

        rows.push(ir_amplification_row(
            "Shared",
            "Residual",
            label,
            sparse_prepared.residual_plan.chunks.len(),
            residual_ir_blocks(&sparse_prepared.residual_plan),
            residual_module("diag_residual", &sparse_prepared.residual_plan)
                .emit_source()
                .len(),
        ));
        rows.push(ir_amplification_row(
            "Sparse",
            "Jacobian",
            label,
            sparse_prepared.jacobian_plan.chunks.len(),
            sparse_ir_blocks(&sparse_prepared.jacobian_plan),
            sparse_module("diag_sparse", &sparse_prepared.jacobian_plan)
                .emit_source()
                .len(),
        ));
        rows.push(ir_amplification_row(
            "Banded",
            "Jacobian",
            label,
            banded_prepared.jacobian_plan.chunks.len(),
            banded_ir_blocks(&banded_prepared.jacobian_plan),
            banded_module("diag_banded", &banded_prepared.jacobian_plan)
                .emit_source()
                .len(),
        ));
    }

    let baselines = ir_amplification_baselines(&rows);
    println!(
        "\n=== Combustion BVP chunk IR/source amplification ===\
         \nn_steps={n_steps}, unknowns={}, residuals={}, sparse_nnz={}\
         \nThis does not execute callbacks. It measures generated IR/source size before scheduling/FFI.",
        jac.vector_of_variables.len(),
        jac.vector_of_functions.len(),
        jac.symbolic_jacobian_sparse_entries_owned().len(),
    );
    println!(
        "{:<8} | {:<8} | {:<7} | {:>6} | {:>8} | {:>10} | {:>8} | {:>8} | {:>9} | {:>8}",
        "matrix",
        "stage",
        "strategy",
        "chunks",
        "outputs",
        "instr",
        "amp",
        "max_blk",
        "temps",
        "src_kb",
    );
    println!("{}", "-".repeat(106));
    for row in &rows {
        let baseline = *baselines
            .get(&(row.matrix, row.stage))
            .expect("whole baseline must exist for every matrix/stage");
        let amp = row.instructions as f64 / baseline as f64;
        println!(
            "{:<8} | {:<8} | {:<7} | {:>6} | {:>8} | {:>10} | {:>8.3} | {:>8} | {:>9} | {:>8.1}",
            row.matrix,
            row.stage,
            row.strategy,
            row.chunks,
            row.outputs,
            row.instructions,
            amp,
            row.max_block_instructions,
            row.temps,
            row.source_kb,
        );
    }

    println!(
        "\nInterpretation: amp > 1 means chunking generated more total straight-line IR than the whole callback. \
         If that happens, runtime parallelism must first pay back duplicated arithmetic/source work plus FFI/scheduler overhead."
    );
}
