type EvalFn<'a> = Box<dyn Fn(&[f64]) -> f64 + Send + Sync + 'a>;

/// Builds a simple two-equation BVP case for performance testing.
///
/// The system consists of equations `y - z` and `-z^3` with border conditions
/// `z(0) = 1` and `y(1) = 1`. The problem is discretized on a uniform grid
/// with `n_steps` intervals using forward differences.
///
/// # Arguments
/// * `n_steps` - Number of grid steps (intervals) for discretization.
///
/// # Returns
/// A [`Jacobian`] containing the symbolic residual functions and sparse Jacobian
/// entries for the discretized system.
fn build_real_bvp_damp1_case(n_steps: usize) -> Jacobian {
    let eq1 = Expr::parse_expression("y-z");
    let eq2 = Expr::parse_expression("-z^3");
    let eq_system = vec![eq1, eq2];
    let values = vec!["z".to_string(), "y".to_string()];

    let mut border_conditions = HashMap::new();
    border_conditions.insert("z".to_string(), vec![(0usize, 1.0f64)]);
    border_conditions.insert("y".to_string(), vec![(1usize, 1.0f64)]);

    let mut jac = Jacobian::new();
    jac.discretization_system_BVP_par(
        eq_system,
        values,
        "x".to_string(),
        0.0,
        Some(n_steps),
        None,
        None,
        border_conditions,
        None,
        None,
        "forward".to_string(),
    );
    jac.calc_jacobian_parallel_smart_optimized();
    jac
}

fn borrowed_sparse_entries(owned_entries: &[(usize, usize, Expr)]) -> Vec<SparseExprEntry<'_>> {
    owned_entries
        .iter()
        .map(|(row, col, expr)| SparseExprEntry {
            row: *row,
            col: *col,
            expr,
        })
        .collect()
}

fn compile_sparse_lambdify<'a>(
    input_names: &[&str],
    owned_entries: &'a [(usize, usize, Expr)],
) -> Vec<EvalFn<'a>> {
    owned_entries
        .iter()
        .map(|(_, _, expr)| {
            let fun = expr.lambdify_borrowed_thread_safe(input_names);
            Box::new(move |args: &[f64]| fun(args)) as EvalFn<'a>
        })
        .collect()
}

fn compile_banded_lambdify<'a>(
    prepared: &'a crate::symbolic::symbolic_functions_BVP::BvpPreparedSparseAotProblem,
) -> Vec<EvalFn<'a>> {
    let banded = prepared.as_prepared_banded_problem();
    let input_names = banded.input_names().to_vec();
    banded
        .jacobian_plan
        .chunks
        .iter()
        .flat_map(|chunk| chunk.entries.iter())
        .map(|entry| {
            let fun = entry
                .expr
                .lambdify_borrowed_thread_safe(input_names.as_slice());
            Box::new(move |args: &[f64]| fun(args)) as EvalFn<'a>
        })
        .collect()
}

fn eval_functions(functions: &[EvalFn<'_>], args: &[f64]) -> Vec<f64> {
    functions.iter().map(|fun| fun(args)).collect()
}

fn banded_assembly_to_dense(assembly: &BandedAssembly) -> DMatrix<f64> {
    let mut dense = DMatrix::zeros(assembly.n(), assembly.n());
    for offset in assembly.min_offset()..=assembly.max_offset() {
        if let Some(diag) = assembly.diag(offset) {
            for (pos, value) in diag.iter().enumerate() {
                let (row, col) = assembly
                    .diag_pos_to_ij(offset, pos)
                    .expect("banded diagonal position should map to dense coordinates");
                dense[(row, col)] = *value;
            }
        }
    }
    dense
}

fn max_abs_diff(lhs: &[f64], rhs: &[f64]) -> f64 {
    lhs.iter()
        .zip(rhs.iter())
        .map(|(a, b)| (a - b).abs())
        .fold(0.0_f64, f64::max)
}

fn sample_rhs(n: usize) -> Vec<f64> {
    (0..n).map(|index| 1.0 + index as f64 * 1.0e-3).collect()
}

fn base_variable_name(name: &str) -> &str {
    name.rsplit_once('_').map_or(name, |(base, _)| base)
}

fn infer_node_major_layout(jac: &Jacobian, n_total: usize) -> NodeMajorLayout {
    let mut seen = HashSet::new();
    let mut vars_per_node = 0usize;
    for name in &jac.variable_string {
        if seen.insert(base_variable_name(name).to_string()) {
            vars_per_node += 1;
        }
    }
    assert!(
        vars_per_node > 0 && n_total % vars_per_node == 0,
        "failed to infer node-major block size from Jacobian variable names"
    );
    NodeMajorLayout::new(n_total / vars_per_node, vars_per_node)
        .expect("node-major layout should be valid")
}

fn preferred_superblock_group(jac: &Jacobian, n_total: usize) -> usize {
    let layout = infer_node_major_layout(jac, n_total);
    let n_nodes = layout.n_nodes();
    let vars_per_node = layout.vars_per_node();

    // The small 2-field damped cases factorize well with the natural
    // node-major block size. For combustion-like 6-field chains we observed
    // that `g=1` is too strict for the block solver, while `g=2` is the best
    // current structured baseline. Keep the policy deliberately simple here so
    // the benchmark answers "does the new solver help?" rather than
    // "can we tune every scenario by hand?".
    if vars_per_node >= 6 && n_nodes % 2 == 0 {
        2
    } else {
        1
    }
}

fn solve_sparse_lu(matrix: &faer::sparse::SparseColMat<usize, f64>, rhs: &[f64]) -> Vec<f64> {
    let lu = matrix
        .sp_lu()
        .expect("sparse LU factorization should succeed");
    let rhs_col = faer::Col::from_fn(rhs.len(), |index| rhs[index]);
    let solution = lu.solve(&rhs_col);
    solution.iter().copied().collect()
}

fn solve_banded_block_tridiagonal(
    jac: &Jacobian,
    assembly: &BandedAssembly,
    rhs: &[f64],
) -> BandedSolveDiagnostics {
    let layout = infer_node_major_layout(jac, assembly.n());
    let block_tridiagonal = assembly
        .to_block_tridiagonal(layout.n_blocks(), layout.block_size())
        .expect("banded assembly should match node-major block-tridiagonal layout");
    let native_attempt = build_linear_solver(
        &block_tridiagonal,
        LinearSolverConfig {
            policy: LinearSolverPolicy::ForceBlockTridiagonal,
            fallback: FallbackPolicy::Never,
            iterative_refinement_steps: 0,
        },
    );
    let (native_block_status, native_block_error) = match native_attempt {
        Ok(_) => ("ok".to_string(), "-".to_string()),
        Err(err) => ("failed".to_string(), format!("{err:?}")),
    };
    let solver = build_linear_solver(
        &block_tridiagonal,
        LinearSolverConfig {
            policy: LinearSolverPolicy::Auto,
            fallback: FallbackPolicy::ToFaerSparse,
            iterative_refinement_steps: 0,
        },
    )
    .expect("banded linear solver factorization should succeed");
    let solver_backend = solver.backend_name().to_string();
    let mut rhs_owned = rhs.to_vec();
    solver
        .solve_in_place(rhs_owned.as_mut_slice())
        .expect("banded linear solve should succeed");
    BandedSolveDiagnostics {
        solution: rhs_owned,
        solver_backend,
        layout,
        native_block_status,
        native_block_error,
    }
}

fn solve_banded_block_tridiagonal_consistent(
    jac: &Jacobian,
    assembly: &BandedAssembly,
    rhs: &[f64],
    refinement_steps: usize,
) -> BandedSolveDiagnostics {
    let node_layout = infer_node_major_layout(jac, assembly.n());
    let nodes_per_superblock = preferred_superblock_group(jac, assembly.n());
    let (layout, block_tridiagonal) = if nodes_per_superblock == 1 {
        (
            node_layout,
            assembly
                .to_block_tridiagonal(node_layout.n_blocks(), node_layout.block_size())
                .expect("banded assembly should match node-major block-tridiagonal layout"),
        )
    } else {
        let super_layout = SuperBlockLayout::new(
            node_layout.n_nodes(),
            node_layout.vars_per_node(),
            nodes_per_superblock,
        )
        .expect("superblock layout should be constructible for consistent solve");
        (
            NodeMajorLayout::new(super_layout.n_blocks(), super_layout.block_size())
                .expect("superblock metadata should map to synthetic node-major layout"),
            assembly
                .finalize_superblock_tridiagonal(&super_layout)
                .expect("banded assembly should match chosen superblock layout"),
        )
    };

    let solver = build_solver_for_system(
        LinearSystemRef::BlockTridiagonal(&block_tridiagonal),
        LinearSolverConfig {
            policy: LinearSolverPolicy::ForceBlockTridiagonalConsistent,
            fallback: FallbackPolicy::Never,
            iterative_refinement_steps: refinement_steps,
        },
    )
    .expect("consistent banded factorization should succeed");
    let mut rhs_owned = rhs.to_vec();
    solver
        .solve_in_place(rhs_owned.as_mut_slice())
        .expect("consistent banded solve should succeed");

    BandedSolveDiagnostics {
        solution: rhs_owned,
        solver_backend: solver.backend_name().to_string(),
        layout,
        native_block_status: "ok".to_string(),
        native_block_error: "-".to_string(),
    }
}

fn eval_ir_blocks_sequential(
    args: &[f64],
    blocks: &[(usize, LinearBlock)],
    output_len: usize,
) -> Vec<f64> {
    let mut out = vec![0.0; output_len];
    for (offset, block) in blocks {
        let len = block.outputs.len();
        block.eval_into(args, &mut out[*offset..(*offset + len)]);
    }
    out
}

fn eval_ir_blocks_parallel(
    args: &[f64],
    blocks: &[(usize, LinearBlock)],
    output_len: usize,
) -> Vec<f64> {
    let mut out = vec![0.0; output_len];
    let pieces: Vec<(usize, Vec<f64>)> = blocks
        .par_iter()
        .map(|(offset, block)| (*offset, block.eval(args)))
        .collect();

    for (offset, values) in pieces {
        out[offset..(offset + values.len())].copy_from_slice(values.as_slice());
    }

    out
}

