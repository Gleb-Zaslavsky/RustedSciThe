        parallel2_residual_samples.push(start.elapsed());

        let start = Instant::now();
        for _ in 0..RESIDUAL_ITERS {
            let out = eval_generated_bvp_residual_col(flat_args.as_slice());
            black_box(out[0]);
            black_box(out.len());
        }
